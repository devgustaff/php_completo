<?php

// retorna o valor absoluto
print "<br>" . abs(-23.8);

// arredonda para cima quando for = 5, < 5 arredonda para baixo
print "<br>" . round(5.5);
print "<br>" . round(5.4);

// ceil - arredonda para cima
print "<br>" . ceil(5.4);

// floor - arredonda para baixo
print "<br>" . floor(5.6);

// retorna um numero aleatorio
echo "<br>" . rand(1, 3);